// buildParamsWithNames builds parameter list with custom names for named dependencies.
// Task 5.2: Uses dependencyNames map to override default parameter names.
func (g *constructorGenerator) buildParamsWithNames(
	fields []detect.FieldInfo, dependencyNames map[string]string,
) string {
	if len(fields) == 0 {
		return ""
	}

	var params []string
	for _, field := range fields {
		// Task 5.2: Check if this field has a custom parameter name
		paramName, hasCustomName := dependencyNames[field.Name]
		if !hasCustomName {
			// Use default parameter name
			paramName = DeriveParamName(field.Name)
		}

		typeStr := typeExprToString(field.TypeExpr)
		params = append(params, fmt.Sprintf("%s %s", paramName, typeStr))
	}

	return strings.Join(params, ", ")
}

// buildAssignmentsWithNames builds field assignments with custom parameter names.
// Task 5.2: Uses dependencyNames map for parameter references in assignments.
func (g *constructorGenerator) buildAssignmentsWithNames(
	fields []detect.FieldInfo, dependencyNames map[string]string,
) []string {
	if len(fields) == 0 {
		return nil
	}

	// Find max field name length for alignment
	maxLen := 0
	for _, field := range fields {
		if len(field.Name) > maxLen {
			maxLen = len(field.Name)
		}
	}

	var assignments []string
	for _, field := range fields {
		// Task 5.2: Use custom parameter name if available
		paramName, hasCustomName := dependencyNames[field.Name]
		if !hasCustomName {
			paramName = DeriveParamName(field.Name)
		}

		// Pad field name for alignment
		padding := strings.Repeat(" ", maxLen-len(field.Name))
		assignments = append(assignments, fmt.Sprintf("%s:%s %s", field.Name, padding, paramName))
	}

	return assignments
}

// formatConstructor formats a constructor code fragment as Go source.
// It wraps the fragment in a package declaration so that format.Source
// treats it as a complete file, preserving blank comment lines.
func formatConstructor(src []byte) ([]byte, error) {
	const prefix = "package _\n\n"
	wrapped := make([]byte, 0, len(prefix)+len(src))
	wrapped = append(wrapped, prefix...)
	wrapped = append(wrapped, src...)

	formatted, err := format.Source(wrapped)
	if err != nil {
		return nil, err
	}

	// Strip the package declaration prefix
	s := string(formatted)
	if idx := strings.Index(s, "\n\n"); idx >= 0 {
		return []byte(s[idx+2:]), nil
	}
	return formatted, nil
}
